package com.cummins.project734.entity;

public class JaccardCoefficient {
	int i;
	int j;
	double coefficientVal;
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}
	public double getCoefficientVal() {
		return coefficientVal;
	}
	public void setCoefficientVal(double coefficientVal) {
		this.coefficientVal = coefficientVal;
	}
}
